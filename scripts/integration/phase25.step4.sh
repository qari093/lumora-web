if npm run 2>/dev/null | grep -q "lint"; then
  echo "‚Üí npm run lint" | tee -a "$LOG_FILE"
  if ! npm run lint >>"$LOG_FILE" 2>&1; then
    echo "‚ö†Ô∏è Lint finished with errors; see $LOG_FILE" | tee -a "$LOG_FILE"
  fi
